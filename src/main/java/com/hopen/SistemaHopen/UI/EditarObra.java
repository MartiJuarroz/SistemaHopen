     private void mostrarTabla() {   
         
        ColorCeldas c = new ColorCeldas();
           
        DefaultTableModel model = new DefaultTableModel(); 
        
        model.addColumn("Id");
        model.addColumn("Cliente");
        model.addColumn("Total presupuesto");
        model.addColumn("Fecha presupuesto");
        
        tablaObras.setModel(model);
        
        String dato[] = new String[4];
        
        PreparedStatement ps;
        
        try{
        Connection con = ConexionDB.getConnection();
        String sql = "SELECT o.id, o.titular,o.total_presupuesto,o.fecha_presupuesto FROM obra o";
        ps = ConexionDB.getConnection().prepareStatement(sql);
        
        ResultSet resultSet = ps.executeQuery();
        
        while(resultSet.next()){
            dato[0] = resultSet.getString(1);
            dato[1] = resultSet.getString(2);
            dato[2] = resultSet.getString(3);
            dato[3] = resultSet.getString(4);
            model.addRow(dato);
        }
        //en el dato[0] estan todos los ids
        
        
        ConexionDB.endConnection(con);
        }catch(Exception e){
            e.printStackTrace();
        }
        
         for (int i = 0; i < tablaObras.getColumnCount(); i++) {
             
             tablaObras.getColumnModel().getColumn(i).setCellRenderer(c);
             
         }
    }
    
    private void buscarDatosObra(String input){
        ColorCeldas c = new ColorCeldas();
        
        DefaultTableModel model = new DefaultTableModel(); 
        
        model.addColumn("Id");
        model.addColumn("Cliente");
        model.addColumn("Total presupuesto");
        model.addColumn("Fecha presupuesto");
        
        tablaObras.setModel(model);
        
        String[] dato = new String[4];
        
        PreparedStatement ps;
        
        try{
        Connection con = ConexionDB.getConnection();
        String sql = "SELECT o.id, o.titular,o.total_presupuesto,o.fecha_presupuesto FROM obra o WHERE titular LIKE ?";
        ps = ConexionDB.getConnection().prepareStatement(sql);
        ps.setString(1, input+"%");
        
        ResultSet resultSet = ps.executeQuery();
        
        while(resultSet.next()){
            dato[0] = resultSet.getString(1);
            dato[1] = resultSet.getString(2);
            dato[2] = resultSet.getString(3);
            dato[3] = resultSet.getString(3);
            model.addRow(dato);
        }
        
        ConexionDB.endConnection(con);
        }catch(Exception e){
            e.printStackTrace();
        }
        
        for (int i = 0; i < tablaObras.getColumnCount(); i++) {
             
             tablaObras.getColumnModel().getColumn(i).setCellRenderer(c);
             
         }
        
    } 
    
    
    private void mostrarDatos(){
      
        if (tablaObras.getSelectedRow() != -1) {
            String titular = (String)tablaObras.getValueAt(tablaObras.getSelectedRow(), 1);
            tfCliente.setText(titular);
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un rengl√≥n primero", "No seleccion√≥ ninguna fila", JOptionPane.INFORMATION_MESSAGE);
        }

        PreparedStatement ps;
        PreparedStatement ps2;
        
        try{
        Connection con = ConexionDB.getConnection();
        String sql = "SELECT * FROM obra o WHERE o.titular=?";
        ps = ConexionDB.getConnection().prepareStatement(sql);
        ps.setString(1, tfCliente.getText());
        
        ResultSet resultSet = ps.executeQuery();
        
        if(resultSet.next()){
            double comision = resultSet.getDouble("comision");
            double costos_fijos = resultSet.getDouble("costos_fijos");
            Date fecha = resultSet.getDate("fecha_presupuesto");
            double ganancia = resultSet.getDouble("ganancia_pretendida");
            double presupuesto = resultSet.getDouble("total_presupuesto"); 
            int idEstado = resultSet.getInt("estado_obra_id");
            idObra = resultSet.getInt("id");
            
             try{
                String sql2 = "SELECT e.nombre_estado_obra FROM estado_obra e WHERE e.id=?";
                ps2 = ConexionDB.getConnection().prepareStatement(sql2);
                ps2.setInt(1, idEstado);
        
                ResultSet resultSet2 = ps2.executeQuery();
                
                 if(resultSet2.next()){
                   String nombreE = resultSet2.getString("nombre_estado_obra");
                   cbEstado.setSelectedItem(nombreE);
                 }
             }catch(Exception e){
                e.printStackTrace();
            }
            CFTxt.setText(Double.toString(costos_fijos));
            ComisionTxt.setText(Double.toString(comision));
            GPTxt.setText(Double.toString(ganancia));
            MTTxt.setText(Double.toString(presupuesto));
            fechaCH.setDate(fecha);
        }
        
        ConexionDB.endConnection(con);
        }catch(Exception e){
            e.printStackTrace();
        }
    }
